CREATE TABLE VEHICULOS(
CODVEHICULO INT(4),
MARCA VARCHAR(20),
MODELO  VARCHAR(40),
TIPO VARCHAR(5),
COLOR VARCHAR(15),
MOTOR VARCHAR(5),
CILINDRADA INT(4),
FECHA DATE,
MATRICULA  VARCHAR(15),
EXTRAS VARCHAR(100),
PRECIO DECIMAL(8,2)
);



CREATE TABLE CLIENTES(
CODCLIENTE INT(4),
NOMBRE VARCHAR(20),
APELLIDOS VARCHAR(40),
DIRECCION VARCHAR(40),
TELEFONO VARCHAR(15),
POBLACION VARCHAR(30),
EDAD INT(3),
CARNET VARCHAR(5)
);



CREATE TABLE VENTA(
CODVENTA INT(4),
CODVEHICULO INT(4),
CODCLIENTE INT(4),
FECHACOMPRA DATE,
TIPOCOMPRA VARCHAR(5),
ASEGURADO VARCHAR(5)
);


ALTER TABLE CLIENTES ADD CONSTRAINT CLIENTES_PK PRIMARY KEY(CODCLIENTE);
ALTER TABLE VEHICULOS ADD CONSTRAINT VEHICULOS_PK PRIMARY KEY(CODVEHICULO);
ALTER TABLE VENTA ADD CONSTRAINT VENTA_PK PRIMARY KEY(CODVENTA);


